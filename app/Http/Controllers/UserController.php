    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::find($id);
        return view('users.show',[
            'title' => 'ユーザー詳細',
            'user'  => $user,
            'posts' => $user->posts()->latest()->get(),
        ]);   
    }

