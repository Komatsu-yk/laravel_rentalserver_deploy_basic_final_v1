     * @return \Illuminate\Http\Response
     */
    public function update(UserRequest $request)
    {
        $user = \Auth::user();
        $user->update($request->only(['name']));
        session()->flash('success', 'プロフィールを編集しました');
        return redirect()->route('users.show', $user);
    }

